const size = Math.sqrt(game.length) //size of array 

const howManyNeighbours = (i) => {
  let neighbours = 0
  const tests = [i - size - 1, i - size, i - size + 1, 
                 i - 1,        /*Cell*/         i + 1,
                 i + size - 1, i + size, i + size + 1]

  tests.forEach(test => {
    if (game[test] === 1) neighbours++
  })


  return neighbours
}

const drawCell = (n, index) => {
  ctx.beginPath()
  if (n === 0) {
    ctx.fillStyle = "rgb(255,255,255)"
    ctx.fillRect(index % size * height / size, Math.floor(index / size) * width / size,
                 height / size, width / size)
  } else {
    ctx.fillStyle = "rgb(0,0,0)"
    ctx.fillRect(index % size * height / size, Math.floor(index / size) * width / size,
                 height / size, width / size)
  }
  ctx.closePath()
}

const drawGrid = () => {
  let index = 1
  ctx.beginPath()

  while (index < size) {
    ctx.fillStyle = "rgb(0,0,0)"
    ctx.fillRect(index * height / size, Math.floor(index / size) * width / size,
                 2, height)
    ctx.fillRect(Math.floor(index / size) * width / size, index * height / size, 
                 height, 2)
    index++
  }

  ctx.closePath()

}

const drawCanvas = () => {
  game = game.map((elem, i) => {
    const neighbours = howManyNeighbours(i)
    if (neighbours < 2) {
      drawCell(0, i)
      return 0
    } else if (neighbours === 2) {
      drawCell(elem, i)
      return elem
    } else if (neighbours === 3)  {
      drawCell(1, i)
      return 1
    } else if (neighbours > 3) {
      drawCell(0, i) 
      return 0
    }
  })
  drawGrid()
  setTimeout(() => requestAnimationFrame(drawCanvas), 250)
}

requestAnimationFrame(drawCanvas)
