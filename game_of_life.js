const size = Math.sqrt(game.length) //size of array 

const howManyNeighbours = (i) => {
  let neighbours = 0
  const tests = [i - size - 1, i - size, i - size + 1, 
                 i - 1, i + 1,
                 i + size - 1, i + size, i + size + 1]

  tests.forEach(test => {
    if (game[test] === 1) neighbours++
  })

  return neighbours
}

const drawSquare = (n, index) => {
  const height = canvas.height
  const width = canvas.width

  ctx.beginPath()
  if (n === 0) {
    ctx.fillStyle = "rgb(250,250,250)"
    ctx.fillRect(index % size * height / size, Math.floor(index / size) * width / size,
                 height / size, width / size)
  } else {
    ctx.fillStyle = "rgb(0,0,0)"
    ctx.fillRect(index % size * height / size, Math.floor(index / size) * width / size,
                 height / size, width / size)
  }
  ctx.closePath()
}

const showCanvas = () => {
  game = game.map((elem, i) => {
    const neighbours = howManyNeighbours(i)
    if (neighbours < 2) {
      drawSquare(0, i)
      return 0
    } else if (neighbours === 2) {
      drawSquare(elem, i)
      return elem
    } else if (neighbours === 3)  {
      drawSquare(1, i)
      return 1
    } else if (neighbours > 3) {
      drawSquare(0, i) 
      return 0
    }
  })
  requestAnimationFrame(showCanvas)
}

